# --------------------------------------------------------------------------
def decode_b64(b64_str):
    '''Decode a base64 JPEG/PNG data-URL to an RGB numpy array.'''
    if ',' in b64_str:
        b64_str = b64_str.split(',', 1)[1]
    raw = base64.b64decode(b64_str)
    arr = np.frombuffer(raw, np.uint8)
    bgr = cv2.imdecode(arr, cv2.IMREAD_COLOR)
    return cv2.cvtColor(bgr, cv2.COLOR_BGR2RGB)

def show_img(img_rgb, title='', figsize=(10, 6)):
    plt.figure(figsize=figsize)
    plt.imshow(img_rgb)
    if title:
        plt.title(title)
    plt.axis('off')
    plt.tight_layout()
    plt.show()

# --------------------------------------------------------------------------
def run_yolo(img_rgb, conf=YOLO_CONF):
    '''Run YOLOv8, return list of detection dicts.'''
    r = yolo_model(img_rgb, conf=conf, verbose=False)[0]
    out = []
    for b in r.boxes:
        out.append({
            'label':      r.names[int(b.cls)],
            'confidence': float(b.conf),
            'bbox':       b.xyxy[0].cpu().numpy().tolist(),  # [x1,y1,x2,y2]
        })
    return out

def draw_boxes(img_rgb, dets):
    out = img_rgb.copy()
    for d in dets:
        x1, y1, x2, y2 = map(int, d['bbox'])
        cv2.rectangle(out, (x1, y1), (x2, y2), (0, 255, 0), 2)
        cv2.putText(out, f"{d['label']} {d['confidence']:.2f}",
                    (x1, max(y1 - 6, 0)), cv2.FONT_HERSHEY_SIMPLEX,
                    0.55, (0, 255, 0), 2)
    return out

# --------------------------------------------------------------------------
def run_sam(img_rgb, dets):
    '''Return one boolean mask (H,W) per detection.'''
    sam_predictor.set_image(img_rgb)
    masks = []
    for d in dets:
        box = np.array(d['bbox'], dtype=float)  # shape (4,)  xyxy
        m, _, _ = sam_predictor.predict(
            point_coords=None,
            point_labels=None,
            box=box,
            multimask_output=False,
        )
        masks.append(m[0].astype(bool))
    return masks

# --------------------------------------------------------------------------
def run_depth(img_rgb):
    '''Return (depth_metres HxW float32, focal_length_px float).'''
    pil  = PILImage.fromarray(img_rgb)
    tens = dp_transform(pil).to(device)
    with torch.no_grad():
        pred = dp_model.infer(tens, f_px=None)
    depth_m = pred['depth'].cpu().numpy().astype(np.float32)
    f_px    = float(pred['focallength_px'])
    return depth_m, f_px

# --------------------------------------------------------------------------
def to_3d(u, v, z, img_hw, f_px):
    h, w  = img_hw
    cx, cy = w / 2.0, h / 2.0
    return (u - cx) * z / f_px, (v - cy) * z / f_px, z

def build_objects(dets, masks, depth_m, f_px):
    '''Merge YOLO detections + SAM masks + Depth Pro into one list.'''
    h, w = depth_m.shape
    objs = []
    for d, mask in zip(dets, masks):
        x1, y1, x2, y2 = d['bbox']
        cx, cy = (x1 + x2) / 2.0, (y1 + y2) / 2.0
        vals   = depth_m[mask]
        z      = float(vals.mean()) if vals.size > 0 else 0.0
        objs.append({
            'label':      d['label'],
            'confidence': d['confidence'],
            'bbox':       d['bbox'],
            'center_px':  (cx, cy),
            'depth_m':    z,
            'xyz':        to_3d(cx, cy, z, (h, w), f_px),
        })
    return objs

