FLOWER_MODEL_PATH = "/content/runs/train/flower_sanjay/weights/best.pt"
flower_model = YOLO(FLOWER_MODEL_PATH)
print("Classes:", flower_model.names)

# ── Color lookup ───────────────────────────────────────────────────────────
COLOR_NAMES = {
    "Red":         (220,  20,  60),
    "Dark Red":    (139,   0,   0),
    "Pink":        (255, 105, 180),
    "Hot Pink":    (255,  20, 147),
    "Orange":      (255, 140,   0),
    "Yellow":      (255, 215,   0),
    "White":       (255, 255, 255),
    "Off White":   (240, 235, 240),
    "Purple":      (128,   0, 128),
    "Dark Purple": ( 75,   0, 130),
    "Violet":      (148,   0, 211),
    "Lavender":    (210, 190, 220),
    "Blue":        ( 30, 144, 255),
    "Light Blue":  (135, 206, 235),
    "Green":       ( 34, 139,  34),
    "Light Green": (144, 238, 144),
    "Brown":       (139,  69,  19),
    "Cream":       (255, 253, 208),
    "Peach":       (255, 218, 185),
    "Coral":       (255, 127,  80),
    "Magenta":     (255,   0, 255),
    "Gray":        (128, 128, 128),
}

def nearest_color(r, g, b):
    best, min_dist = "Unknown", float("inf")
    for name, (cr, cg, cb) in COLOR_NAMES.items():
        d = (r-cr)**2 + (g-cg)**2 + (b-cb)**2
        if d < min_dist:
            min_dist, best = d, name
    return best

# ── Upload flower image ────────────────────────────────────────────────────
print("\nUpload a flower image:")
up      = files.upload()
fname   = list(up.keys())[0]
arr     = np.frombuffer(list(up.values())[0], np.uint8)
img_rgb = cv2.cvtColor(cv2.imdecode(arr, cv2.IMREAD_COLOR), cv2.COLOR_BGR2RGB)

# ── YOLO detect ───────────────────────────────────────────────────────────
dets = flower_model(img_rgb, conf=0.5, iou=0.4)[0]
if not dets.boxes:
    dets = flower_model(img_rgb, conf=0.1, iou=0.4)[0]
    print(f"Retry conf=0.1 → {len(dets.boxes)} found")
else:
    print(f"Flowers found: {len(dets.boxes)}")

if not dets.boxes:
    print("No flower detected.")
else:
    sam_predictor.set_image(img_rgb)
    total = len(dets.boxes)
    fig, axes = plt.subplots(total, 7, figsize=(20, 5 * total))
    if total == 1:
        axes = [axes]

    for idx, box in enumerate(dets.boxes):
        x1, y1, x2, y2 = map(int, box.xyxy[0])
        cls_name = flower_model.names[int(box.cls)]
        conf     = float(box.conf)

        # SAM mask
        mask, _, _ = sam_predictor.predict(
            box=np.array([x1, y1, x2, y2], dtype=float),
            multimask_output=False,
        )
        flower_mask = mask[0].astype(bool)

        # K-Means on flower pixels only
        pixels = img_rgb[flower_mask].astype(np.float32)
        valid  = pixels[
            ~(((pixels[:,0]>245) & (pixels[:,1]>245) & (pixels[:,2]>245)) |
              ((pixels[:,0]< 20) & (pixels[:,1]< 20) & (pixels[:,2]< 20)))
        ]
        if len(valid) < 50:
            valid = pixels


        km = KMeans(n_clusters=5, n_init=10, random_state=42)
        km.fit(valid)
        centers   = km.cluster_centers_.astype(int)
        _, counts = np.unique(km.labels_, return_counts=True)
        pcts      = counts / counts.sum() * 100
        order     = np.argsort(-pcts)
        centers   = centers[order]
        pcts      = pcts[order]

        r0, g0, b0 = int(centers[0][0]), int(centers[0][1]), int(centers[0][2])
        main_color = nearest_color(r0, g0, b0)

        # Draw
        vis = img_rgb.copy()
        cv2.rectangle(vis, (x1,y1),(x2,y2),(0,255,0),3)
        cv2.putText(vis, f"{cls_name} | {main_color}",
                    (x1, max(y1-8,0)), cv2.FONT_HERSHEY_SIMPLEX, 0.8,(0,255,0),2)
        vis[flower_mask] = (vis[flower_mask]*0.5 + np.array([0,255,100])*0.5).astype(np.uint8)

        axes[idx][0].imshow(vis)
        axes[idx][0].set_title(f"{cls_name}\n{main_color}  conf={conf:.2f}", fontsize=9)
        axes[idx][0].axis("off")

        print(f"\nFlower {idx+1} [{cls_name}]  conf={conf:.2f}  → {main_color}")
        print("─" * 42)

        for i in range(5):
            r, g, b = int(centers[i][0]), int(centers[i][1]), int(centers[i][2])
            cname   = nearest_color(r, g, b)
            pct     = pcts[i]
            swatch  = np.full((80,80,3), [r,g,b], dtype=np.uint8)
            axes[idx][i+1].imshow(swatch)
            axes[idx][i+1].set_title(f"{cname}\n{pct:.1f}%\nRGB({r},{g},{b})", fontsize=8)
            axes[idx][i+1].axis("off")
            print(f"  {i+1}. {cname:<13} {pct:5.1f}%   RGB({r},{g},{b})")

    plt.suptitle("Flower Detection + Color Analysis  (YOLOv8 + SAM + K-Means)",
                 fontsize=13, fontweight="bold")
    plt.tight_layout()
    plt.show()

